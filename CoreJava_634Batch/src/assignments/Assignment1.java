package assignments;

public class Assignment1 {

	public static void main(String[] args) {

		for (int i = 1; i <= 100; i++) {
			// Verify i is Even or Odd?
			if (i % 2 == 0) {
				System.out.println(i + " is Even number");
			} else {
				System.out.println(i + " is Odd number");
			}

		}

	}

}
